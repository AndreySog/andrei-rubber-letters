#!/usr/bin/env python3
"""
gost_5915_table.py — таблица параметров гаек ГОСТ 5915-70 (шестигранные, класс точности В).

⚠️ SOURCE: https://glavkrep.ru/gost-din/din-700-999/gost-5915-70/
⚠️ Не официальная публикация; TODO: верифицировать по docs.cntd.ru

Обозначения по ГОСТ 5915-70:
  d  — номинальный диаметр резьбы (= внутренний диаметр гайки)
  P  — шаг резьбы (крупный)
  m  — высота гайки (= h на чертеже)
  S  — размер под ключ (= размер шестигранника)
  e  — диаметр описанной окружности (≈ D гайки для чертежа)
  c  — фаска (~0.15·d)

Для eskd.py:
  d, m (h), S, D (=e), c, d_thread (d — отверстие для подписи "М...")
  + d_thread_hole (визуальный диаметр отверстия ≈1.05·d)
  + D1_circle (окружность фаски на боковом виде ≈0.95·S — НЕ касается вершин гексагона)
"""
import math


GOST_5915_70 = [
    # d,  P,    m,    S,    e,    c
    (6,  1.0,  5.0,  10,   10.9, 0.9),
    (8,  1.25, 6.5,  13,   14.2, 1.2),
    (10, 1.5,  8.0,  17,   18.7, 1.5),
    (12, 1.75, 10.0, 19,   20.9, 1.8),
    (14, 2.0,  11.0, 22,   24.0, 2.1),
    (16, 2.0,  13.0, 24,   26.2, 2.4),
    (18, 2.5,  15.0, 27,   29.6, 2.7),
    (20, 2.5,  16.0, 30,   33.0, 3.0),
    (22, 2.5,  18.0, 32,   35.0, 3.3),
    (24, 3.0,  19.0, 36,   39.6, 3.6),
    (27, 3.0,  22.0, 41,   45.2, 4.0),
    (30, 3.5,  24.0, 46,   50.9, 4.5),
    (36, 4.0,  29.0, 55,   60.8, 5.4),
    (42, 4.5,  34.0, 65,   71.3, 6.3),
    (48, 5.0,  38.0, 75,   82.6, 7.2),
]


def get_params(d):
    """Возвращает dict параметров гайки по её диаметру d."""
    for d_g, P, m, S, e, c in GOST_5915_70:
        if d_g == d:
            return {
                "d": d, "m": m, "S": S, "D": e, "c": c, "P": P,
                "d_thread_hole": round(d * 1.05, 2),
                "D1_circle": round(S * 0.95, 2),
            }
    raise ValueError(f"d={d} не в размерном ряде ГОСТ 5915-70")


if __name__ == "__main__":
    print(f"ГОСТ 5915-70 — {len(GOST_5915_70)} диаметров")
    for d in [8, 10, 16, 20]:
        p = get_params(d)
        print(f"  М{d}: m={p['m']} S={p['S']} D={p['D']} c={p['c']} P={p['P']}")
