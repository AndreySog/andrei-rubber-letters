def get_params(d, l):
    """
    Возвращает (l, l0, S, D, h, c) для болта диаметром d и длиной l.
    l0 = b (длина резьбы из ГОСТ), D = e (диаметр описанной окружности),
    h = k (высота головки), c = фаска торца (~0.15·d).
    """
    for d_gost, P, k, S, e, b in GOST_7798_70:
        if d_gost == d:
            c = round(0.15 * d, 1)
            return {
                "d": d, "l": l, "l0": b, "S": S,
                "D": e, "h": k, "c": c, "P": P,
            }
    raise ValueError(f"d={d} не в размерном ряде ГОСТ 7798-70")


def all_sizes():
    """Все (d, l) пары из ГОСТ."""
    out = []
    for d, P, k, S, e, b in GOST_7798_70:
        for l in L_BY_D[d]:
            out.append((d, l))
    return out


